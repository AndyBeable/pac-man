// Grid legend
// 0 - pac dots
// 1 - wall
// 2 - ghost-lair
// 3 - power-pellet
// 4 - empty

const squares = [];
// Draw grid and render it

function createBoard() {
  for (let i = 0; i < layout.length; i++) {
    const square = document.createElement('div'); // creates div for all 784 squares
    grid.appendChild(square); // add those squares to our grid
    squares.push(square); // push squares to the new empy 'squares' array above.

    if (layout[i] === 0) {
      // styling the layout. If number in the layout is 0, find those squares in the sqaures array and add the relevant styles
      squares[i].classList.add('pac-dot');
    } else if (layout[i] === 1) {
      squares[i].classList.add('wall');
    } else if (layout[i] === 3) {
      squares[i].classList.add('power-pellet');
    }
  }
}
createBoard();

//starting position of pac-man

